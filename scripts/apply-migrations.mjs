/**
 * Apply pending Supabase migrations.
 * Connects directly via PG using the service_role key as the postgres password.
 *
 * Usage: node scripts/apply-migrations.mjs
 */
import { readFileSync, readdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'
import { createHash } from 'crypto'
import pg from 'pg'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')

// Load env
const envPath = join(root, '.env.local')
const envContent = readFileSync(envPath, 'utf-8')
const env = Object.fromEntries(
  envContent
    .split('\n')
    .filter(line => line && !line.startsWith('#'))
    .map(line => line.split('='))
    .filter(parts => parts.length >= 2)
    .map(([k, ...v]) => [k.trim(), v.join('=').trim()])
)

const supabaseUrl = env.NEXT_PUBLIC_SUPABASE_URL
const serviceRoleKey = env.SUPABASE_SERVICE_ROLE_KEY

if (!supabaseUrl || !serviceRoleKey) {
  console.error('Missing Supabase environment variables')
  process.exit(1)
}

const match = supabaseUrl.match(/https:\/\/(.+)\.supabase\.co/)
if (!match) {
  console.error('Could not parse Supabase project ref from URL')
  process.exit(1)
}
const projectRef = match[1]

async function findConnection() {
  // Read the correct pooler URL from .temp (generated by supabase link)
  const poolerUrlPath = join(root, 'supabase', '.temp', 'pooler-url')
  let poolerUrl
  try {
    poolerUrl = readFileSync(poolerUrlPath, 'utf-8').trim()
  } catch { /* fallback below */ }

  // Build pooler connection with service_role key as password
  const user = `postgres.${projectRef}`
  const hosts = poolerUrl
    ? [new URL(poolerUrl).host]
    : ['aws-1-eu-central-1.pooler.supabase.com', 'aws-0-us-west-1.pooler.supabase.com']

  for (const host of hosts) {
    const cs = `postgresql://${user}:${encodeURIComponent(serviceRoleKey)}@${host}:5432/postgres?connect_timeout=10`
    const p = new pg.Pool({ connectionString: cs, max: 1, idleTimeoutMillis: 10000 })
    try {
      const c = await p.connect()
      await c.query('SELECT 1')
      c.release()
      console.log(`  ✓ Connected via ${host} (pooler)`)
      return p
    } catch (e) {
      console.log(`  ✗ ${host}: ${e.message}`)
      await p.end()
    }
  }
  return null
}

async function main() {
  console.log('Finding Supabase database host...')
  const pool = await findConnection()
  if (!pool) {
    console.error('Could not connect to any known Supabase host format.')
    process.exit(1)
  }

  const client = await pool.connect()
  try {
    await client.query(`
      CREATE TABLE IF NOT EXISTS supabase_migrations (
        filename    TEXT PRIMARY KEY,
        hash        TEXT,
        created_at  TIMESTAMPTZ DEFAULT NOW()
      )
    `)

    const { rows: existing } = await client.query(
      `SELECT filename FROM supabase_migrations ORDER BY filename ASC`
    )
    const applied = new Set(existing.map(r => r.filename))
    console.log(`\nAlready applied: ${applied.size} migrations`)

    const migrationsDir = join(root, 'supabase', 'migrations')
    const files = readdirSync(migrationsDir)
      .filter(f => f.endsWith('.sql'))
      .sort()

    let appliedCount = 0
    for (const file of files) {
      if (applied.has(file)) {
        console.log(`  SKIP  ${file}`)
        continue
      }

      const sql = readFileSync(join(migrationsDir, file), 'utf-8')

      process.stdout.write(`  APPLY ${file}...`)
      try {
        await client.query(sql)
        const hash = createHash('md5').update(sql).digest('hex').substring(0, 8)
        await client.query(
          `INSERT INTO supabase_migrations (filename, hash, created_at)
           VALUES ($1, $2, NOW())`,
          [file, hash]
        )
        console.log(' OK')
        appliedCount++
      } catch (err) {
        console.log(` FAILED: ${err.message}`)
        console.log(`  SQL (first 300 chars):`)
        console.log(`  ${sql.substring(0, 300).replace(/\n/g, '\n  ')}`)
        throw err
      }
    }

    console.log(`\nApplied ${appliedCount} new migrations. Done.`)
  } finally {
    client.release()
    await pool.end()
  }
}

main().catch(err => {
  console.error('Migration failed:', err.message)
  process.exit(1)
})
